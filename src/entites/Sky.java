package entites;

public class Sky {

	private float red;
	private float green;
	private float blue;

	public Sky(float red, float green, float blue) {
		this.red = red;
		this.green = green;
		this.blue = blue;
	}
	
	public float getRed(){
		return red;
	}
	
	public float getGreen(){
		return green;
	}
	
	public float getBlue(){
		return blue;
	}
}
